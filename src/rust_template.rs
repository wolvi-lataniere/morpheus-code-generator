fn map_inst_feed_rust(inst: &yaml_parser::InstFeedback) -> String {
    format!("{{{}}}",
        inst.parameters.iter().map(|v|
        format!("{}: {}", v.name, v.data_type.to_rust_type_string())).collect::<Vec<String>>().join(","))
}

